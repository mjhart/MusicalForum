Musical Forum is an entirely student run group at Brown University dedicated to the production of musical theater by the student body. Each semester we produce one full length, previously produced musical plus various Revues and Cabarets.&nbsp;<br />
<br />
If you or your Brown student group wishes to borrow MF&#39;s supplies or instruments, please contact <a href="mailto:anne_kocher@brown.edu">Annie</a>. Musical Forum inspects all equipment before and after use by other student groups. If you are interested in borrowing equipment, note that a deposit check will be required, which will be used to pay for damages to items should they be returned in less than equal condition to when borrowed.</div>
			<div class="cushycms" id="text">Musical Forum, which celebrated its silver anniversary in 2012, is an entirely student run group at Brown University dedicated to the production of musical theatre by the student body. Our mission is to bring exciting, entirely student produced musical theatre to the Brown University campus, while nurturing budding directors, designers, and actors. We produce one full-length musical each semester in T.F. Green, in addition to a concert version or revue during commencement. We also organize and produce cabaret nights, workshops, and other musical theatre opportunities throughout the academic year.<br />
<br />
Originally called &quot;The Musical Forum,&quot; MF was founded in 1987. Musical Forum&#39;s first production, You&#39;re a Good Man, Charlie Brown, was mounted in March of that year. Since then, we have produced over 50 full-length musicals and supported hundreds of student theatre artists. For 25 years, Musical Forum has allowed professional quality musical theatre produced by students for students to thrive on Brown&#39;s campus.<br />
<br />
Notable alumni include Tony Award winner John Lloyd Young, New York Musical Theatre Festival executive producer Isaac Hurwtiz, playwright Stephen Karam, Broadway actor Jed Resnick, and Charlie Alterman, musical director of the upcoming Broadway revival of Godspell and the Pulitzer Prize-winning Next to Normal.<br />
