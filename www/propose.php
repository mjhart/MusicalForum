PROPOSALS for the Fall 2014 slot due at 5:00 PM, MARCH 31, 2014.</div>
			<div id="text" class="cushycms">The Proposal Guidelines that follow provide practical information about the show and the process, and offer a sense of what we will want to learn from your proposal. In general, the more specific information you can give us, the better.<br />
<br />
General Information<br />
<br />
RIGHTS<br />
If you are considering proposing a certain show, contact <a href="mailto:rebecca_lichtin@brown.edu">Becca Lichtin</a> regarding rights as soon as possible. Let her know the title of the show, version (if there is more than one), and the company that controls the rights to it (if you know). Do not contact licensing companies directly, as this could severely delay the delivery of materials for the show once it gets passed.<br />
<br />
Proposal Outline<br />
SHOW INFORMATION (brief)<br />
The name of the show, author, composer, etc.<br />
<br />
CONTACT INFORMATION<br />
Your name, phone number, and box number<br />
<br />
STAFF<br />
The staff of a show is crucial to the success of the process. With whom are you working to make this show happen? The list below includes some of the major staff positions that must be filled, but it is only a partial list. Keep in mind that there are many other people involved in making a show go up. The more positions you have filled, the better. Talk to us early on if you are having trouble filling positions: we will be happy to try to help you find people to fill the spaces, but we need time to do it!<br />
<br />
Musical Director<br />
Choreographer<br />
Assistant Director<br />
Production Manager<br />
Stage Manager (and ASMs)<br />
Costume Designer<br />
Set Designer<br />
Lighting Designer<br />
Sound Designer<br />
Technical Director (and ATDs)<br />
<br />
BUDGET<br />
The budget for the show is $750. There is a separate copying budget of $90 for posters and programs. (You don&#39;t need to address the copying budget in your proposal). Theatre rental and rights are taken care of by Musical Forum and do not come out of your budget. Let us know how you plan on allocating the budget to your different expenses. We understand that you may not be able to give us a complete and accurate budget, but we want you to give us a general estimation of the budget breakdown. Typical expenses include set, costumes, and props. If you have any particularly expensive or noteworthy props (e.g. gun rentals or a large moose head), you may want to include them as a separate budget item. Allow some extra money in case of emergencies. Things tend to go wrong/break at the last minute.<br />
<br />
SET<br />
We want to see a set design. It does not have to be perfect or exact, but it must give us a good idea of what your set will look like. Keep in mind you only have $750 for the show. Don&#39;t design an expensive set if you want to costume your show and have props.<br />
<br />
THE PROCESS<br />
Tell us how you want to work the process. What kind of director are you? How will rehearsals be run? Do you like to jump right into the script and music or spend time on exercises? This is a short rehearsal process &mdash; how will you deal with time constraints? How are you going to deal with the combination of acting, music and dance? There are no right or wrong answers to these questions, and you don&#39;t need to answer everything &mdash; these are just some ideas. Essentially, we want to get an idea of how a show will run with you, as director, at its helm.<br />
<br />
THE SHOW<br />
This is where you tell us about your vision. Why are you proposing this show? What is your concept of the show? How will you, as a director, work with present themes? What potential problems, if any, do you foresee in the text, and how will you plan to deal with them? Again, there are no right or wrong answers to these questions. Tell us as much as you can about the show &mdash; what it means to you and what your vision of it is. The more you can tell us, the better.<br />
<br />
BOARD HELP<br />
If you have any questions, or need help, several resources are available to you. We have established a mentor program for proposals. Let <a href="mailto:karin_nilo@brown.edu">Karin</a>&nbsp;know as soon as you&#39;re thinking of proposing, and she will assign you a proposal mentor from the board. Your proposal mentor is your liasion to the board during the proposal process. He or she can answer any questions you have and help you look for staff. Your mentor can also read over your proposal before you submit the final version. This will give you a chance to hear some of the questions and concerns the board is likely to have and address them in your final proposal.</div>
